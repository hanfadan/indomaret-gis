<p align="justify">
Menurut Prahasta (2007), WebGIS adalah aplikasi GIS atau pemetaan digital yang memanfaatkan jaringan internet sebagai media komunikasi yang berfungsi mendistribusikan, mempublikasikan, mengintegrasikan, mengkomunikasikan dan menyediakan informasi dalam bentuk teks, peta dijital serta menjalankan fungsiâ€“fungsi analisis dan query yang terkait dengan GIS melalui jaringan internet. Sedangkan menurut Setiawan dan Rabbasa, penggunaan data spasial dirasakan semakin diperlukan untuk berbagai keperluan seperti penelitian, pengembangan dan perencanaan wilayah, serta manajemen sumber daya alam.
<br>
</br>
Pengguna data spasial merasakan minimnya informasi mengenai keberadaan dan ketersediaan data spasial yang dibutuhkan. Penyebaran (diseminasi) data spasial yang selama ini dilakukan dengan menggunakan media yang telah ada yang meliputi media cetak (peta), cd-rom, dan media penyimpanan lainnya dirasakan kurang mencukupi kebutuhan pengguna. Pengguna diharuskan datang dan melihat langsung data tersebut pada tempatnya (data provider). Hal ini mengurangi mobilitas dan kecepatan dalam memperoleh informasi mengenai data tersebut. Karena itu dirasakan perlu adanya WebGIS.
</p>
